def seed_default_prompts(db: Session) -> None:
    """DB에 기본 프롬프트가 없으면 시딩."""
    for prompt_data in DEFAULT_PROMPTS:
        existing = db.query(PromptTemplate).filter_by(
            analysis_type=prompt_data["analysis_type"]
        ).first()
        if not existing:
            db.add(PromptTemplate(**prompt_data))
    db.commit()
